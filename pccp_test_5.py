#1. 둥근 완두 순동(RR)자가 수분을 하면 다음 세대는 둥근 완두 순종 형질만 나타난다
#2. 주름진 완두 순종(rr)자가 수분을 하면 다음 세대는 주름진 완두 순종 형질만 나타난다
#3. 두 순종을 교배한 잡종(Rr)을 자가 수분할 경우, 다음 세대의 형질은 RR:Rr:rr=1:2:1
#의 비율로 나타난다

#몇세대 몇번째 완두는 무엇인가

#문제 이해
#RR: 둥근 완두 순종
#rr: 주름진 완두 순종
#Rr: 둥근 완두 잡종 (이형접합)

#멘델의 법칙:
#RR × RR 교배 → 모두 RR (둥근)
#rr × rr 교배 → 모두 rr (주름진)
#Rr × Rr 자가 수분 → 다음 세대 RR:Rr:rr = 1:2:1 비율
#queries

#일반적으로 두 신분이 이루는 각도는 한 바퀴를 360도로 하여 표현합니다.
